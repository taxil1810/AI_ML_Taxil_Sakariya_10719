# program : 40 Student Grade Calculator


print("=== Student Grade Calculator ===")

# Input data with type casting
name = input("Student का नाम: ")
subject1 = float(input("subject 1 marks: "))
subject2 = float(input("subject 2 marks: "))
subject3 = float(input("subject 3 marks: "))

# Calculations using operators
total_marks = subject1 + subject2 + subject3
max_marks = 300.0
percentage = (total_marks / max_marks) * 100

# Comparison operators for grade
grade_A = percentage >= 90
grade_B = percentage >= 80 and percentage < 90  
grade_C = percentage >= 60 and percentage < 80
pass_status = percentage >= 40

# Results
print("\n=== Results ===")
print("Student:", name)
print("Total Marks:", total_marks)
print("Percentage:", round(percentage, 2), "%")
print("Grade A:", grade_A)
print("Grade B:", grade_B) 
print("Grade C:", grade_C)
print("Pass Status:", pass_status)

# Type and id information
print("\n=== Technical Info ===")
print("Name type:", type(name), "id:", id(name))
print("Percentage type:", type(percentage), "id:", id(percentage))
print("Pass status type:", type(pass_status), "id:", id(pass_status))